package oky.frcomment.db;

public class FrComment {
	private int fcm_no;
	private String id;
	private String fcm_content;
	private String fcm_reg_date;
	private int fcm_comment_board_num;
	private int fcm_comment_re_ref; 
	private int fcm_comment_re_lev;
	private int fcm_comment_re_seq;
	
	
	public int getFcm_no() {
		return fcm_no;
	}
	public void setFcm_no(int fcm_no) {
		this.fcm_no = fcm_no;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getFcm_content() {
		return fcm_content;
	}
	public void setFcm_content(String fcm_content) {
		this.fcm_content = fcm_content;
	}
	public String getFcm_reg_date() {
		return fcm_reg_date;
	}
	public void setFcm_reg_date(String fcm_reg_date) {
		this.fcm_reg_date = fcm_reg_date;
	}
	public int getFcm_comment_board_num() {
		return fcm_comment_board_num;
	}
	public void setFcm_comment_board_num(int fcm_comment_board_num) {
		this.fcm_comment_board_num = fcm_comment_board_num;
	}
	public int getFcm_comment_re_ref() {
		return fcm_comment_re_ref;
	}
	public void setFcm_comment_re_ref(int fcm_comment_re_ref) {
		this.fcm_comment_re_ref = fcm_comment_re_ref;
	}
	public int getFcm_comment_re_lev() {
		return fcm_comment_re_lev;
	}
	public void setFcm_comment_re_lev(int fcm_comment_re_lev) {
		this.fcm_comment_re_lev = fcm_comment_re_lev;
	}
	public int getFcm_comment_re_seq() {
		return fcm_comment_re_seq;
	}
	public void setFcm_comment_re_seq(int fcm_comment_re_seq) {
		this.fcm_comment_re_seq = fcm_comment_re_seq;
	}
	
	
}
